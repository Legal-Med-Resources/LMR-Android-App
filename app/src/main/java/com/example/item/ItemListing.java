package com.example.item;

public class ItemListing {

    private String id;
    private String listingName;
    private String listingDescription;
    private String listingAddress;
    private String listingEmail;
    private String listingPhone;
    private String listingWebsite;
    private String listingLatitude;
    private String listingLongitude;
    private String listingImageB;
    private String categoryName;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getListingName() {
        return listingName;
    }

    public void setListingName(String listingName) {
        this.listingName = listingName;
    }

    public String getListingDescription() {
        return listingDescription;
    }

    public void setListingDescription(String listingDescription) {
        this.listingDescription = listingDescription;
    }

    public String getListingAddress() {
        return listingAddress;
    }

    public void setListingAddress(String listingAddress) {
        this.listingAddress = listingAddress;
    }

    public String getListingEmail() {
        return listingEmail;
    }

    public void setListingEmail(String listingEmail) {
        this.listingEmail = listingEmail;
    }

    public String getListingPhone() {
        return listingPhone;
    }

    public void setListingPhone(String listingPhone) {
        this.listingPhone = listingPhone;
    }

    public String getListingWebsite() {
        return listingWebsite;
    }

    public void setListingWebsite(String listingWebsite) {
        this.listingWebsite = listingWebsite;
    }

    public String getListingLatitude() {
        return listingLatitude;
    }

    public void setListingLatitude(String listingLatitude) {
        this.listingLatitude = listingLatitude;
    }

    public String getListingLongitude() {
        return listingLongitude;
    }

    public void setListingLongitude(String listingLongitude) {
        this.listingLongitude = listingLongitude;
    }

    public String getListingImageB() {
        return listingImageB;
    }

    public void setListingImageB(String listingImageB) {
        this.listingImageB = listingImageB;
    }

    public String getCategoryName() {
        return categoryName;
    }

    public void setCategoryName(String categoryName) {
        this.categoryName = categoryName;
    }

}
